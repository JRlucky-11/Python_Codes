print("[___ArithMatic_Operations___]\n")

Frist_num = 15
Second_num = 2
power = 2
Devider = 3
Modula_Divider = 4

Sum_of_2_num = Frist_num + Second_num
Def_of_2_num = Frist_num - Second_num
Mul_of_2_num = Frist_num * Second_num
Dev_of_2_num = Frist_num / Second_num
Square_of_num = Frist_num ** power
Floar_Dev_of_num = Frist_num // Devider
Modula_Div_of_num = Frist_num % Modula_Divider

print("1st num is: ", Frist_num)
print("2nd num is: ", Second_num)
print("Power^: ", power)
print("Devider//: ", Devider, "\n")
print("Modula_Division_%: ", Modula_Divider)

print("Sum = ", Frist_num, "+", Second_num, "=>", Sum_of_2_num)
print("Def = ", Frist_num, "-", Second_num, "=>", Def_of_2_num)
print("Mul = ", Frist_num, "*", Second_num, "=>", Mul_of_2_num)
print("Dev = ", Frist_num, "/", Second_num, "=>", Dev_of_2_num)
print("Square = ", Frist_num, "**", power, "=>", Frist_num, "^", power, "=>", Square_of_num)
print("Floar_Dev = ", Frist_num, "//", Devider, "=>", Floar_Dev_of_num)
print("Modula_Division[Reminder] = ", Frist_num, "%", Modula_Divider, "=>", Modula_Div_of_num)