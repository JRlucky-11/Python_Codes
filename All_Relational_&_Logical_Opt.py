num1 = 15
num2 = 15
print("num1 = ", num1)
print("num2 = ", num2)
print("Relational Part")
print("\t|> num1 > num2: ", num1 > num2)
print("\t|> num1 < num2: ", num1 < num2)
print("\t|> num1 >= num2: ", num1 >= num2)
print("\t|> num1 <= num2: ", num1 <= num2)
print("\t|> num1 == num2: ", num1 == num2)
print("\t|> num1 != num2: ", num1 != num2)
print("\n")

print("Logical Part")
print("\t|> num1 > 10 and num2 > 10: ", num1 > 10 and num2 > 10)
print("\t|> num1 > 10 or num2 < 10: ", num1 > 10 or num2 < 10)